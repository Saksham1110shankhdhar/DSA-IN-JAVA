public class VolumeOfSphere {
    public static void main(String[] args) {
        System.out.println("Welcome to the Volume of Sphere Calculator");

        double radius = 5.0; // Example radius

        double voulme = 4.0/3 * Math.PI * Math.pow(radius, 3);
        System.out.println("The volume of the sphere with radius " + radius + " is: " + voulme);
    }
    
}
